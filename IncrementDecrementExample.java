public class IncrementDecrementExample {
   public static void main(String[] args) {
      // Increment and decrement with int
      int a = 10;
      System.out.println("Original value of a: " + a);
      a++; // increment a by 1
      System.out.println("After incrementing a: " + a);
      a--; // decrement a by 1
      System.out.println("After decrementing a: " + a);

      // Increment and decrement with float
      float b = 3.5f;
      System.out.println("Original value of b: " + b);
      b++; // increment b by 1
      System.out.println("After incrementing b: " + b);
      b--; // decrement b by 1
      System.out.println("After decrementing b: " + b);

      // Increment and decrement with double
      double c = 7.8;
      System.out.println("Original value of c: " + c);
      c++; // increment c by 1
      System.out.println("After incrementing c: " + c);
      c--; // decrement c by 1
      System.out.println("After decrementing c: " + c);

      // Increment and decrement with long
      long d = 100L;
      System.out.println("Original value of d: " + d);
      d++; // increment d by 1
      System.out.println("After incrementing d: " + d);
      d--; // decrement d by 1
      System.out.println("After decrementing d: " + d);

      // Increment and decrement with byte
      byte e = 5;
      System.out.println("Original value of e: " + e);
      e++; // increment e by 1
      System.out.println("After incrementing e: " + e);
      e--; // decrement e by 1
      System.out.println("After decrementing e: " + e);

      // Increment and decrement with short
      short f = 10;
      System.out.println("Original value of f: " + f);
      f++; // increment f by 1
      System.out.println("After incrementing f: " + f);
      f--; // decrement f by 1
      System.out.println("After decrementing f: " + f);

      // Increment and decrement with char
      char g = 'A';
      System.out.println("Original value of g: " + g);
      g++; // increment g by 1
      System.out.println("After incrementing g: " + g);
      g--; // decrement g by 1
      System.out.println("After decrementing g: " + g);
   }
}